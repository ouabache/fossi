#include "Range.h"

//-----------------------------------------------------------------------------
// Function: Range::Range()
//-----------------------------------------------------------------------------
Range::Range(QString const& left, QString const& right) :
left_(left), right_(right)
{

}

//-----------------------------------------------------------------------------
// Function: Range::~Range()
//-----------------------------------------------------------------------------
Range::~Range()
{

}

//-----------------------------------------------------------------------------
// Function: Range::getLeft()
//-----------------------------------------------------------------------------
QString Range::getLeft() const
{
    return left_;
}

//-----------------------------------------------------------------------------
// Function: Range::getRight()
//-----------------------------------------------------------------------------
QString Range::getRight() const
{
    return right_;
}

//-----------------------------------------------------------------------------
// Function: Range::setLeft()
//-----------------------------------------------------------------------------
void Range::setLeft(QString const& newLeft)
{
    left_ = newLeft;
}

//-----------------------------------------------------------------------------
// Function: Range::setRight()
//-----------------------------------------------------------------------------
void Range::setRight(QString const& newright)
{
    right_ = newright;
}